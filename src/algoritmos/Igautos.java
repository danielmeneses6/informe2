    private void ordenar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ordenar1ActionPerformed
       String auxmarca,auxcolor;
       int auxmodelo,auxmodelo2,auxmodelo3;
       float auxkm,auxkm2,auxkm3;
       TableModel model = tabla.getModel();
       int z=model.getColumnCount();
       for(int x=0;x<z;x++)
       {
           for(int j=0;j<z;j++)
           {
           auxkm2=(float) model.getValueAt(j, 4);
           auxkm3=(float) model.getValueAt(j+1, 4);
       if(auxkm2>auxkm3)
       {
           
           auxmarca=(String) model.getValueAt(j,1 );
           auxmodelo= (int) model.getValueAt(j,2 );
           auxcolor=(String) model.getValueAt(j, 3);
           auxkm= (float) model.getValueAt(j,4 );
           model.setValueAt(model.getValueAt(1+j, 1),j,1 );
           model.setValueAt(model.getValueAt(1+j, 2),j,2 );
           model.setValueAt(model.getValueAt(1+j, 3),j,3 );
           model.setValueAt(model.getValueAt(1+j, 4),j,4 );
           model.setValueAt(auxmarca,j+1,1 );
           model.setValueAt(auxmodelo,j+1,2 );
           model.setValueAt(auxcolor,j+1,3 );
           model.setValueAt(auxkm,j+1,4 );
          
       }
           }
       }
    }//GEN-LAST:event_ordenar1ActionPerformed

